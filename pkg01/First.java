package pkg01;


public class First {

	public static void main(String[] args) {
		// S 대문자, 프린트엘엔
		// 처음으로 프로그램을 작성해 봅니다.
		System.out.println("홍 길동");
		System.out.println("안녕하세요.");
		
		System.out.print("동해물과"); // 애국가 출력 시작
		System.out.print("백두산이"); // 애국가 출력 종료 
		System.out.println("문자열 결합 : " + 10 + 20);
		System.out.println("덧셈 : " + (10 + 20));
		
		System.out.println("이름\t국어\t영어");
		System.out.println("홍길동\t50\t70");
		System.out.println("박영희\t60\t80");
		
		System.out.println("제 이름은 \'홍길동\' 입니다.");
		System.out.print("엔터키\n\n\n3번누르기");
	}

}
